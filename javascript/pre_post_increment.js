// Post Increment
var a = 10

var b = a++ // First assign and then increment

console.log('b = ' + b)
console.log('a = ' + a)

// Pre increment
var a = 10
var b = ++a // First increment and then assign

console.log('b = ' + b)
console.log('a = ' + a)


